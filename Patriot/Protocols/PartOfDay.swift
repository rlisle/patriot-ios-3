import Foundation

enum PartOfDay: Int {
    case unknown = 0
    case Sunrise = 1
    case Morning = 2
    case Noon = 3
    case Afternoon = 4
    case Sunset = 5
    case Dusk = 6
    case Night = 7
    case Dawn = 8
}
